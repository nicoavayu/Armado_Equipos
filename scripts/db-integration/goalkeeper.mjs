#!/usr/bin/env node
// Focused DB integration test for "múltiples posiciones + arquero".
//
// Boots an embedded Postgres, applies a minimal self-contained stub of the prod
// core (usuarios/partidos/jugadores/notifications/match_join_requests/profiles +
// the geo helpers the feature reuses), then applies the real feature migration
// `20260718120000_multi_positions_and_goalkeeper_availability.sql` and exercises
// its invariants against real SQL: the positions trigger, the role-aware
// approval RPC, and the deduped goalkeeper notification fan-out.
//
// Uso: npm run test:db:goalkeeper

