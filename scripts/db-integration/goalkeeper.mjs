// Capacity + concurrency scenarios: a pool of plain field players.
const CAP_ADMIN = '00000000-0000-4000-8000-0000000000d0';
const CAP_USERS = Array.from({ length: 8 }, (_, i) =>
  `00000000-0000-4000-8000-0000000000e${i}`);
