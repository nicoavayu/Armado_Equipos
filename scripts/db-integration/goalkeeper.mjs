const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..', '..');
const MIGRATION = '20260718120000_multi_positions_and_goalkeeper_availability.sql';

const PORT = 54800 + Math.floor(Math.random() * 400);
const DB_NAME = 'arma2_goalkeeper';
const DATA_DIR = fs.mkdtempSync(path.join(os.tmpdir(), 'arma2-gk-pg-'));

const ADMIN = '00000000-0000-4000-8000-0000000000a1';
const GK_NEAR = '00000000-0000-4000-8000-0000000000b1';
const GK_FAR = '00000000-0000-4000-8000-0000000000b2';
const GK_OFF = '00000000-0000-4000-8000-0000000000b3';
